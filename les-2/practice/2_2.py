score = int(input('Geef je score: '));

if(score >= 18):
    print('Gefeliciteerd!');
    print('Met een score van ' + str(score) + ' ben je geslaagd!');
else:
    print('Helaas!');
    print('Met een score van ' + str(score) + ' ben je niet geslaagd. Probeer het volgend jaar opnieuw.');
